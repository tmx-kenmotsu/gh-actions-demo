package examples.calculator;

public class Calc {
    public int add(int i1, int i2) {
        return i1 + i2;
    }

    public int sub(int i1, int i2) {
        return i1 - i2;
    }

    public int mul(int i1, int i2) {
        return i1 * i2;
    }

    public int div(int i1, int i2) {
        return i1 / i2;
    }
}
