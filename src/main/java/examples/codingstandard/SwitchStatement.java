package examples.codingstandard;

public class SwitchStatement {
    void method (int i) {
        switch (i) {
            case 0:
                System.out.println ("Zero");
                break;
            case 1:
                System.out.println ("One");
                break;
            case 2:
                System.out.println ("Two");
                break;
        }
    }
}
